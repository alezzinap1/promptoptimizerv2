"""
Директивы для быстрых меток стиля (режим «Фото») — семантика вместо сырого списка id.
Согласовано с IMAGE_STYLE_BADGES на главной.
"""
from __future__ import annotations

IMAGE_TAG_INSTRUCTIONS: dict[str, str] = {
    "realism": "Приоритет фотореализма: правдоподобный свет, текстуры материалов, физика освещения без «пластикового» блеска.",
    "minimal": "Минималистичная композиция: мало объектов, много воздуха, чистые линии, сдержанная палитра.",
    "cartoon": "Стилизация под мультфильм: упрощённые формы, ясные контуры, выразительные силуэты.",
    "cinematic": "Киношная подача: кадрирование как в кино, атмосфера кадра, глубина резкости, драматический свет.",
    "illustration": "Иллюстративный стиль: читаемый рисунок, возможно книжная или редакционная подача.",
    "3d_render": "3D-рендер: читаемые объём, материалы, студийный или постановочный свет.",
    "dark_mood": "Тёмная атмосфера: приглушённый свет, высокий контраст теней, настроение без пересвета.",
    "bright_palette": "Светлая палитра: воздух, мягкие блики, открытое настроение, без грязного серого.",
}


def expand_image_tags_to_directives(tag_ids: list[str]) -> str:
    """Превращает список id меток в блок директив для user-брифа."""
    if not tag_ids:
        return ""
    lines: list[str] = []
    unknown: list[str] = []
    for tid in tag_ids:
        key = str(tid).strip().lower()
        instr = IMAGE_TAG_INSTRUCTIONS.get(key)
        if instr:
            lines.append(f"- ({key}) {instr}")
        else:
            unknown.append(key)
    out = "[Директивы по выбранным меткам стиля — учти в визуальной подаче, не перечёркивая сюжет пользователя:]\n"
    out += "\n".join(lines)
    if unknown:
        out += "\n- Дополнительные метки (интерпретируй умеренно): " + ", ".join(unknown)
    return out
