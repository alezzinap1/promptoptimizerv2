def expand_image_tags_to_directives(tag_ids: list[str]) -> str:
    """Превращает список id меток в блок директив для user-брифа."""
    if not tag_ids:
        return ""
    lines: list[str] = []
    unknown: list[str] = []
    for tid in tag_ids:
        key = str(tid).strip().lower()
        instr = IMAGE_TAG_INSTRUCTIONS.get(key)
        if instr:
            lines.append(f"- ({key}) {instr}")
        else:
            unknown.append(key)
    out = "[Директивы по выбранным меткам стиля — учти в визуальной подаче, не перечёркивая сюжет пользователя:]\n"
    out += "\n".join(lines)
    if unknown:
        out += "\n- Дополнительные метки (интерпретируй умеренно): " + ", ".join(unknown)
    return out
